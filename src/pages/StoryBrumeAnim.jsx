/*stop secondary audio*/
export function stopIt2(sound) {
  let sound0 = document.getElementById("audioP");
  sound0.volume = 0.7;
  sound.volume = 0.07;
  setTimeout(() => {
    sound.volume = 0.04;
  }, 500);
  setTimeout(() => {
    sound.volume = 0.01;
  }, 750);
  setTimeout(() => {
    sound.volume = 0.01;
    sound.pause();
  }, 1000);
}
