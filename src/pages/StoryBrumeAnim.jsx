/*play secondary audio*/
export function playIt2(sound) {
  sound.volume = 0.01;
  setTimeout(() => {
    sound.play();
  }, 500);
  setTimeout(() => {
    sound.volume = 0.03;
  }, 750);
  setTimeout(() => {
    sound.volume = 0.05;
  }, 1000);
  setTimeout(() => {
    sound.volume = 0.08;
  }, 1250);
  setTimeout(() => {
    sound.volume = 0.1;
  }, 1500);
  setTimeout(() => {
    sound.volume = 0.1;
    stopIt2(sound);
  }, 9000);
}
